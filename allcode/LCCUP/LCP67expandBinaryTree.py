class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right
class Solution:
    def expandBinaryTree(self, root: Optional[TreeNode]) -> Optional[TreeNode]:
        def dfs(node):
            if node.left:
                ln = TreeNode(-1, left=node.left)
                node.left = ln
                dfs(ln.left)
            if node.right:
                rn = TreeNode(val=-1, right=node.right)
                node.right = rn
                dfs(rn.right)
        dfs(root)
        return root


