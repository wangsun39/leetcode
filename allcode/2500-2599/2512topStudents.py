class Solution:
    def topStudents(self, positive_feedback: List[str], negative_feedback: List[str], report: List[str], student_id: List[int], k: int) -> List[int]:
        sp, sn = set(positive_feedback), set(negative_feedback)
        def calc(rpt):
            w = rpt.split()
            res = 0
            for x in w:
                if x in sp:
                    res += 3
                elif x in sn:
                    res -= 1
            return res
        scores = [calc(rpt) for rpt in report]
        pair = sorted(zip(scores, student_id), key=lambda x: [-x[0],x[1]])
        return [x[1] for x in pair[:k]]



