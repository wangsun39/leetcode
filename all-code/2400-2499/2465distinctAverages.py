class Solution:
    def distinctAverages(self, nums: List[int]) -> int:
        n = len(nums)
        nums.sort()
        s = set()
        for i in range(n // 2):
            a, b = nums[0], nums[-1]
            s.add((a + b) / 2)
            nums.pop(0)
            nums.pop()
        return len(s)



so = Solution()
