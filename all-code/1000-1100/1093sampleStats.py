from math import inf
from typing import List
from bisect import *

class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        nums = [[i, x] for i, x in enumerate(count) if x != 0]
        mn, mx = nums[0][0], nums[-1][0]
        s = sum(x * i for i, x in nums)
        n = sum(count)
        acc = [[-1, -1]]  # 前缀和 []
        for i, x in nums:
            acc.append([x + acc[-1][0], i])
        if (n % 2) & 1:
            pos = bisect_left(acc, n // 2, key=lambda x: x[0])
            median = acc[pos][1]
        else:
            p1, p2 = bisect_left(acc, n // 2, key=lambda x: x[0]), bisect_left(acc, n // 2 - 1, key=lambda x: x[0])
            median = (acc[p1][1] + acc[p2][1]) / 2

        nums.sort(key=lambda x:x[1])
        mode = nums[-1][0]
        return [mn, mx, s / n, median, mode]


