        primes = []
        def all_prime(mi, mx):  # 获取[mi, mx) 内所有质数
            if mx < 2:
                return 0
            isPrime = [1] * mx
            isPrime[0] = isPrime[1] = 0
            for i in range(2, int(mx ** 0.5) + 1):
                if isPrime[i]:
                    isPrime[i * i:mx:i] = [0] * ((mx - 1 - i * i) // i + 1)
            for i in range(mi, mx):
                if isPrime[i]:
                    primes.append(i)
        all_prime(2, 1001)
        def foreach(num):
            for i in primes:
                if i > num:
                    break
                if num % i == 0:
                    s.add(i)
        for num in nums:
            foreach(num)
        # print(s)
        return len(s)


