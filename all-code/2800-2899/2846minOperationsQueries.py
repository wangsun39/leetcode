class Solution:
    def minOperationsQueries(self, n: int, edges: List[List[int]], queries: List[List[int]]) -> List[int]:
        g = defaultdict(list)
        w = {}
        for x, y, z in edges:
            g[x].append(y)
            g[y].append(x)
            w[(x, y)] = w[(y, x)] = z - 1
        dep = [0] * n  # 节点深度

        m = int(math.log(n, 2)) + 1
        ac = [[0] * m for _ in range(n)] # ac[i][j] 表示第i个节点的2^j个祖先
        cnt = [[[0] * 26 for _ in range(m)] for _ in range(n)]  # cnt[i][j][k]  第i个点到它的第2^i个祖先之间权重为k出现的次数

        def dfs(x, p):
            for y in g[x]:
                if y == p: continue
                dep[y] = dep[x] + 1
                ac[y][0] = x
                cnt[y][0][w[(x, y)]] = 1
                dfs(y, x)
        dfs(0, -1)

        for j in range(1, m):
            for i in range(n):
                f = ac[i][j - 1]  # i的第2^(j-1)个祖先
                if f == 0: continue
                ac[i][j] = ac[f][j - 1]
                for k in range(26):
                    cnt[i][j][k] = cnt[i][j - 1][k] + cnt[f][j - 1][k]

        def lca(x0, y0):
            if dep[x0] > dep[y0]:
                x0, y0 = y0, x0
            x, y = x0, y0
            counter = [0] * 26
            diff = dep[y] - dep[x]
            i = 0
            while diff:
                if diff & 1:
                    for j in range(26):
                        counter[j] += cnt[y][i][j]
                    y = ac[y][i]
                i += 1
                diff >>= 1
            if x == y:
                t = x
            else:
                for i in range(m - 1, -1, -1):
                    if ac[x][i] == ac[y][i]:
                        continue
                    for j in range(26):
                        counter[j] += (cnt[x][i][j] + cnt[y][i][j])
                    x, y = ac[x][i], ac[y][i]
                t = ac[x][0]  # lca 节点
                wx, wy = w[(x, ac[x][0])], w[(y, ac[y][0])]
                counter[wx] += cnt[x][0][wx]
                counter[wy] += cnt[y][0][wy]
            dxy = dep[x0] + dep[y0] - dep[t] * 2  # x,y 的距离
            return dxy - max(counter)

        ans = [0] * len(queries)
        for i, [x, y] in enumerate(queries):
            ans[i] = lca(x, y)

        return ans


